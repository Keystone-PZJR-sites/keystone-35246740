function DesktopCircle({ platform }: { platform: SocialPlatform }) {
  const { viewBox, w, h, x, y, content } = DESKTOP_ICONS[platform];
  return (
    <svg width="48" height="48" viewBox="0 0 48 48" fill="none" xmlns="http://www.w3.org/2000/svg" aria-hidden="true">
      <circle cx="24" cy="24" r="24" fill="var(--color-work-accent)" />
      <svg x={x} y={y} width={w} height={h} viewBox={viewBox} overflow="visible">
        {content}
      </svg>
    </svg>
  );
}

export function SocialIcon({ platform, href, variant, className }: SocialIconProps) {
  const label = LABELS[platform];

  if (variant === 'desktop') {
    if (href) {
      return (
        <a href={href} aria-label={label} target="_blank" rel="noopener noreferrer" className={className}>
          <DesktopCircle platform={platform} />
        </a>
      );
    }
    return (
      <span aria-hidden="true" className={className}>
        <DesktopCircle platform={platform} />
      </span>
    );
  }

  if (!href) return null;
  return (
    <a href={href} aria-label={label} target="_blank" rel="noopener noreferrer" className={className}>
      <svg width="16" height="16" viewBox="0 0 16 16" fill="none" xmlns="http://www.w3.org/2000/svg" aria-hidden="true">
        <MobileGlyph platform={platform} />
      </svg>
    </a>
  );
}
